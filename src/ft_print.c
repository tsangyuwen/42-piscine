#include "bsq.h"

void	ft_print(void)
{
	int j;
	int k;

	j = 0;
	while (g_map[j] != 0)
	{
		k = 0;
		while (g_map[j][k] != '\0')
		{
			printf("%c", g_map[j][k]);
			k++;
		}
		printf("\n");
		j++;
	}
}
